# 북 동 남 서
dr = [-1, 0, 1, 0]  # 행 변화량 (row)
dc = [ 0, 1, 0, -1] # 열 변화량 (column)

def get_count_of_departments_cleaned_by_robot_vacuum(r, c, d, room_map):
    n = len(room_map)
    m = len(room_map[0])
    count_of_departments_cleaned = 1
    room_map[r][c] = 2
    queue = deque([(r, c, d)])

    while queue:
        r, c, d = queue.popleft()
        temp_d = d

        for i in range(4):
            temp_d = get_d_index_when_rotate_left(temp_d)
            new_r, new_c = r + dr[temp_d], c + dc[temp_d]

            if 0 <= new_r < n and 0 <= new_c < m and room_map[new_r][new_c] == 0:
                count_of_departments_cleaned += 1
                room_map[new_r][new_c] = 2  # 청소한 곳은 2로 표시
                queue.append((new_r, new_c, temp_d))
                break

            elif i == 3:  # 네 방향 모두 청소가 되어있거나 벽인 경우
                temp_d = get_d_index_when_go_back(d)
                new_r, new_c = r + dr[temp_d], c + dc[temp_d]
                queue.append((new_r, new_c, d))

                if room_map[new_r][new_c] == 1:  # 뒤쪽이 벽인 경우
                    return count_of_departments_cleaned  # 청소를 종료하고 현재까지 청소한 개수를 반환

def get_d_index_when_rotate_left(d):
    return (d + 3) % 4

def get_d_index_when_go_back(d):
    return (d + 2) % 4

